        // Game state
        let currentCategory = '';
        let currentQuestions = [];
        let currentQuestionIndex = 0;
        let score = 0;
        let timeLeft = 30;
        let timerInterval;
        let correctAnswersCount = 0;

        // DOM elements
        const startScreen = document.getElementById('startScreen');
        const quizScreen = document.getElementById('quizScreen');
        const resultsScreen = document.getElementById('resultsScreen');

        // Start quiz
        function startQuiz(category) {
            currentCategory = category;
            currentQuestions = shuffleArray([...quizData[category]]);
            currentQuestionIndex = 0;
            score = 0;
            correctAnswersCount = 0;
            
            startScreen.classList.add('hidden');
            quizScreen.classList.remove('hidden');
            
            document.getElementById('totalQuestions').textContent = currentQuestions.length;
            loadQuestion();
        }

        // Shuffle array
        function shuffleArray(array) {
            for (let i = array.length - 1; i > 0; i--) {
                const j = Math.floor(Math.random() * (i + 1));
                [array[i], array[j]] = [array[j], array[i]];
            }
            return array;
        }

        // Load current question
        function loadQuestion() {
            const question = currentQuestions[currentQuestionIndex];
            document.getElementById('currentQuestion').textContent = currentQuestionIndex + 1;
            document.getElementById('questionText').textContent = question.question;
            document.getElementById('currentScore').textContent = score;
            
            // Update progress bar
            const progress = ((currentQuestionIndex + 1) / currentQuestions.length) * 100;
            document.getElementById('progressBar').style.width = progress + '%';
            
            // Load options
            const optionsContainer = document.getElementById('optionsContainer');
            optionsContainer.innerHTML = '';
            
            question.options.forEach((option, index) => {
                const button = document.createElement('button');
                button.className = 'w-full bg-white bg-opacity-20 hover:bg-opacity-30 text-white font-bold py-4 px-6 rounded-lg quiz-option text-left';
                button.innerHTML = `<span class="mr-3">${String.fromCharCode(65 + index)}.</span>${option}`;
                button.onclick = () => selectAnswer(index);
                optionsContainer.appendChild(button);
            });
            
            // Start timer
            timeLeft = 30;
            document.getElementById('timer').textContent = timeLeft;
            clearInterval(timerInterval);
            timerInterval = setInterval(updateTimer, 1000);
        }

        // Update timer
        function updateTimer() {
            timeLeft--;
            document.getElementById('timer').textContent = timeLeft;
            
            if (timeLeft <= 0) {
                clearInterval(timerInterval);
                selectAnswer(-1); // Time up
            }
        }

        // Select answer
        function selectAnswer(selectedIndex) {
            clearInterval(timerInterval);
            const question = currentQuestions[currentQuestionIndex];
            const options = document.querySelectorAll('#optionsContainer button');
            
            // Disable all options
            options.forEach(option => option.classList.add('disabled'));
            
            // Show correct/incorrect answers
            options.forEach((option, index) => {
                if (index === question.correct) {
                    option.classList.add('correct');
                } else if (index === selectedIndex && selectedIndex !== question.correct) {
                    option.classList.add('incorrect');
                }
            });
            
            // Update score
            if (selectedIndex === question.correct) {
                score += 10;
                correctAnswersCount++;
                document.getElementById('currentScore').textContent = score;
            }
            
            // Move to next question after delay
            setTimeout(() => {
                currentQuestionIndex++;
                if (currentQuestionIndex < currentQuestions.length) {
                    loadQuestion();
                } else {
                    showResults();
                }
            }, 2000);
        }

        // Show results
        function showResults() {
            quizScreen.classList.add('hidden');
            resultsScreen.classList.remove('hidden');
            
            const totalQuestions = currentQuestions.length;
            const accuracy = Math.round((correctAnswersCount / totalQuestions) * 100);
            
            document.getElementById('finalScore').textContent = score;
            document.getElementById('correctAnswers').textContent = correctAnswersCount;
            document.getElementById('totalAnswered').textContent = totalQuestions;
            document.getElementById('accuracy').textContent = accuracy + '%';
            
            // Set result message and icon
            const resultIcon = document.getElementById('resultIcon');
            const resultMessage = document.getElementById('resultMessage');
            
            if (accuracy >= 80) {
                resultIcon.className = 'fas fa-trophy text-6xl text-yellow-400 mb-4 bounce';
                resultMessage.textContent = 'Excellent! You\'re a quiz master!';
            } else if (accuracy >= 60) {
                resultIcon.className = 'fas fa-medal text-6xl text-yellow-400 mb-4 bounce';
                resultMessage.textContent = 'Great job! Well done!';
            } else if (accuracy >= 40) {
                resultIcon.className = 'fas fa-thumbs-up text-6xl text-yellow-400 mb-4 bounce';
                resultMessage.textContent = 'Good effort! Keep practicing!';
            } else {
                resultIcon.className = 'fas fa-heart text-6xl text-yellow-400 mb-4 bounce';
                resultMessage.textContent = 'Keep trying! You\'ll get better!';
            }
        }

        // Restart quiz with same category
        function restartQuiz() {
            startQuiz(currentCategory);
        }

        // Go back to home screen
        function goHome() {
            resultsScreen.classList.add('hidden');
            startScreen.classList.remove('hidden');
        }

